    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed

        //1. Capturar as informações digitadas pelo usuário
        String nome = txtNomeCompleto.getText();
        String cep = txtCep.getText().trim();
        String endereco = txtEndereco.getText();
        String fone = txtTelefone.getText().trim();
        String email = txtEmail.getText();

        String foneCobaia = txtTelefone.getText();

        foneCobaia = foneCobaia.replaceAll("[()\\s-]", "").trim();

        //2. Verificar se não há campos vazios + tratamentos
        if (nome.isEmpty()) {
            String mensagem = "Nome Completo não informado!";
            JOptionPane.showMessageDialog(rootPane, mensagem);
            txtNomeCompleto.grabFocus();
        } else if (cep.isEmpty()) {
            String mensagem = "CEP não informado!";
            JOptionPane.showMessageDialog(rootPane, mensagem);
            txtCep.grabFocus();
        } else if (foneCobaia.isEmpty()) {
            String mensagem = "Telefone não informado!";
            JOptionPane.showMessageDialog(rootPane, mensagem);
            txtTelefone.grabFocus();
        } else if (email.isEmpty() || email.contains("@gmail") == false) {
            String mensagem = "Email não informado ou inválido!";
            JOptionPane.showMessageDialog(rootPane, mensagem);
            txtEmail.grabFocus();
        } else {
            //3; Enviar os dados para o Controller

            buscadorCep operCep = new buscadorCep();
            String cepParaEndereco = txtCep.getText();

            if (txtEndereco.isEditable() == true && endereco.isEmpty() == true) {
                JOptionPane.showMessageDialog(null, "Endereço não informado");

            } else if (txtEndereco.isEditable() == true && endereco.isEmpty() == false) {
                endereco = txtEndereco.getText();
                CadastroUsuarioController op = new CadastroUsuarioController();
                op.insereRegistroCadastroUsuarioController(nome, cep, endereco, fone, email);

                TelaOperCadastroClientes tela = new TelaOperCadastroClientes();

                tela.setVisible(true);
                tela.atualizaFechar();
                this.dispose();

                //3. Criar os dados para o Controller
            } else if (operCep.encontraCep(cepParaEndereco) == "") {
                JOptionPane.showMessageDialog(null, "CEP inválido ou site indisponível");

            } else {
                txtEndereco.setText(operCep.encontraCep(cepParaEndereco));
                endereco = txtEndereco.getText();
                CadastroUsuarioController op = new CadastroUsuarioController();
                op.insereRegistroCadastroUsuarioController(nome, cep, endereco, fone, email);

                TelaOperCadastroClientes tela = new TelaOperCadastroClientes();

                tela.setVisible(true);
                tela.atualizaFechar();
                this.dispose();

                //3. Criar os dados para o Controller
            }

        }


    }//GEN-LAST:event_btnSalvarActionPerformed

